    public function addColumns(): PowerGridColumns
    {
        return PowerGrid::columns()
            ->addColumn('id')
            ->addColumn('plan', fn (PlanProfit $model) => $model->plan->name)
            ->addColumn('profit')
            ->addColumn('direct_commission')
            ->addColumn('created_at_formatted', fn (PlanProfit $model) => Carbon::parse($model->created_at)->format('d/m/Y H:i:s'));
    }

