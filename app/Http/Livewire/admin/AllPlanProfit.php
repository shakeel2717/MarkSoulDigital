    {
        return [
            Column::make('Plan id', 'plan'),
            Column::make('Profit', 'profit')
                ->sortable()
                ->editOnClick()
                ->searchable(),

            Column::make('Direct commission', 'direct_commission')
                ->sortable()
                ->editOnClick()
                ->searchable(),

            Column::make('Binary commission', 'binary_commission')
                ->sortable()
                ->editOnClick()
                ->searchable(),

        ];
    }

